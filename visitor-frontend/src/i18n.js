export function createTranslator(languageCode) {
  const lc = (languageCode || 'en').toLowerCase()
  let code = 'en'
  if (lc.startsWith('vi')) code = 'vi'
  else if (lc.startsWith('zh')) code = 'zh'
  else if (lc.startsWith('ja')) code = 'ja'
  else if (lc.startsWith('ko')) code = 'ko'
  const dict = messages[code]

  return (key, fallback) => dict[key] || fallback || key
}
